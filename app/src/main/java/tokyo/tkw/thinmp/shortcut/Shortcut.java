package tokyo.tkw.thinmp.shortcut;

public class Shortcut {
    public int id;
    public String name;
    public String type;
    public String albumArtId;

    public Shortcut(int id, String name, String type, String albumArtId) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.albumArtId = albumArtId;
    }
}
